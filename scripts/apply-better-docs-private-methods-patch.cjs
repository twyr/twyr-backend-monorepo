function lines(parts) {
	return `${parts.join('\n')}\n`;
}

function replaceOnce(contents, searchValue, replacementValue, label) {
	const options = arguments[4] || {};
	const alreadyAppliedValues = options.alreadyAppliedValues || [];
	const optional = options.optional || false;
	const appliedValues = [replacementValue, ...alreadyAppliedValues].filter(
		Boolean
	);

	if (appliedValues.some((value) => contents.includes(value))) {
		return {
			contents,
			changed: false
		};
	}

	if (contents.includes(searchValue)) {
		return {
			contents: contents.replace(searchValue, replacementValue),
			changed: true
		};
	}

	if (!replacementValue) {
		return {
			contents,
			changed: false
		};
	}

	if (optional) {
		return {
			contents,
			changed: false
		};
	}

	if (!contents.includes(searchValue)) {
		throw new Error(
			`[better-docs-patch] Failed: expected snippet not found for ${label}.`
		);
	}
}

function patchFile(filePath, replacements) {
	// eslint-disable-next-line security/detect-non-literal-fs-filename
	if (!fs.existsSync(filePath)) {
		throw new Error(
			`[better-docs-patch] Failed: file not found at ${filePath}`
		);
	}

	// eslint-disable-next-line security/detect-non-literal-fs-filename
	let contents = fs.readFileSync(filePath, 'utf8');
	let changed = false;

	for (const replacement of replacements) {
		const result = replaceOnce(
			contents,
			replacement.searchValue,
			replacement.replacementValue,
			replacement.label,
			{
				alreadyAppliedValues: replacement.alreadyAppliedValues,
				optional: replacement.optional
			}
		);

		contents = result.contents;
		changed = changed || result.changed;
	}

	if (changed) {
		// eslint-disable-next-line security/detect-non-literal-fs-filename
		fs.writeFileSync(filePath, contents, 'utf8');
	}

	return changed;
}

